import streamlit as st
import pandas as pd
import numpy as np
import torch
import torch.nn.functional as F
from PIL import Image
import joblib
from torchvision import models, transforms
import matplotlib.pyplot as plt
import cv2

# ==========================
# Config
# ==========================
CLASS_NAMES = ["cancer", "lesion", "normal", "others"]
IMG_SIZE = 224
DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")

# ==========================
# Load Models
# ==========================
@st.cache_resource
def load_tabular_model():
    model = joblib.load("tabular_model.pkl")
    scaler = joblib.load("scaler.pkl")
    return model, scaler

@st.cache_resource
def load_cnn_model():
    cnn_model = models.efficientnet_b0(pretrained=False)
    cnn_model.classifier = torch.nn.Sequential(
        torch.nn.Dropout(0.4),
        torch.nn.Linear(cnn_model.classifier[1].in_features, len(CLASS_NAMES))
    )
    cnn_model.load_state_dict(torch.load("best_cnn_model.pth", map_location=DEVICE))
    cnn_model.to(DEVICE)
    cnn_model.eval()
    return cnn_model

tab_model, scaler = load_tabular_model()
cnn_model = load_cnn_model()

# ==========================
# Transforms
# ==========================
cnn_transform = transforms.Compose([
    transforms.Resize((IMG_SIZE, IMG_SIZE)),
    transforms.ToTensor(),
    transforms.Normalize(mean=[0.485,0.456,0.406], std=[0.229,0.224,0.225])
])

# ==========================
# Streamlit App
# ==========================
st.set_page_config(page_title="Cervical Cancer Predictor", layout="centered")
st.title("🩺 Cervical Cancer Risk Prediction — Multimodal")

# ----- Tabular Input -----
st.header("Patient Risk Factors")
# Example: assuming your features are numeric, adjust names to your dataset
feature_names = ['Age', 'Number of sexual partners', 'First sexual intercourse',
                 'Num of pregnancies', 'Smokes', 'Hormonal Contraceptives',
                 'IUD', 'STDs']
user_input = {}
for f in feature_names:
    user_input[f] = st.number_input(f, min_value=0, max_value=100, value=0)

tab_df = pd.DataFrame([user_input])
tab_scaled = scaler.transform(tab_df)
tab_prob = tab_model.predict_proba(tab_scaled)[:,1]

# ----- Image Input -----
st.header("Pap Smear Image")
uploaded_file = st.file_uploader("Upload Pap Smear Image (jpg/png)", type=["jpg","jpeg","png"])
if uploaded_file:
    img = Image.open(uploaded_file).convert("RGB")
    st.image(img, caption="Uploaded Image", use_column_width=True)

    # CNN prediction
    img_tensor = cnn_transform(img).unsqueeze(0).to(DEVICE)
    with torch.no_grad():
        outputs = cnn_model(img_tensor)
        probs = F.softmax(outputs, dim=1)
        cnn_prob = (probs[:,0] + probs[:,1]).cpu().numpy()[0]  # cancer + lesion

    # ----- Fusion -----
    final_score = 0.6 * tab_prob[0] + 0.4 * cnn_prob
    fusion_threshold = 0.45
    risk_pred = "High Risk" if final_score >= fusion_threshold else "Low Risk"

    st.subheader("✅ Prediction Results")
    st.write(f"**Tabular Model Risk Probability:** {tab_prob[0]:.3f}")
    st.write(f"**CNN Image Risk Probability:** {cnn_prob:.3f}")
    st.write(f"**Final Risk Score (Fusion):** {final_score:.3f}")
    st.write(f"**Predicted Risk:** {risk_pred}")

    # ----- Optional Grad-CAM -----
    if st.checkbox("Show Grad-CAM Heatmap"):
        gradients = None
        activations = None

        def forward_hook(module, inp, out):
            global activations
            activations = out
        def backward_hook(module, grad_in, grad_out):
            global gradients
            gradients = grad_out[0]

        target_layer = cnn_model.features[-1]
        target_layer.register_forward_hook(forward_hook)
        target_layer.register_backward_hook(backward_hook)

        cnn_model.zero_grad()
        output = cnn_model(img_tensor)
        pred_class = output.argmax()
        output[0, pred_class].backward()

        pooled_gradients = torch.mean(gradients, dim=[0,2,3])
        activ = activations.detach().cpu()[0]
        for i in range(len(pooled_gradients)):
            activ[i] *= pooled_gradients[i].cpu()
        heatmap = torch.mean(activ, dim=0).numpy()
        heatmap = np.maximum(heatmap,0)
        heatmap /= heatmap.max()
        heatmap = cv2.resize(heatmap, (IMG_SIZE, IMG_SIZE))
        heatmap = cv2.applyColorMap(np.uint8(255*heatmap), cv2.COLORMAP_JET)/255
        img_np = np.array(img.resize((IMG_SIZE, IMG_SIZE)))/255
        superimposed = heatmap*0.4 + img_np
        st.image(superimposed, caption="Grad-CAM Heatmap", use_column_width=True)

